/**
 * Seed official squads (16 players) for all 48 WC 2026 teams in migration 0020.
 * Run: node scripts/generate-wc2026-squad-migration.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const ROSTER = [
  { shirt: 1, pos: 'GK', group: 'GK' },
  { shirt: 2, pos: 'RB', group: 'DEF' },
  { shirt: 3, pos: 'CB', group: 'DEF' },
  { shirt: 4, pos: 'CB', group: 'DEF' },
  { shirt: 5, pos: 'LB', group: 'DEF' },
  { shirt: 6, pos: 'DM', group: 'MID' },
  { shirt: 7, pos: 'CM', group: 'MID' },
  { shirt: 8, pos: 'CM', group: 'MID' },
  { shirt: 10, pos: 'AM', group: 'MID' },
  { shirt: 11, pos: 'LW', group: 'FWD' },
  { shirt: 14, pos: 'RW', group: 'FWD' },
  { shirt: 9, pos: 'ST', group: 'FWD' },
  { shirt: 12, pos: 'GK', group: 'GK', sub: true },
  { shirt: 13, pos: 'CB', group: 'DEF', sub: true },
  { shirt: 15, pos: 'CM', group: 'MID', sub: true },
  { shirt: 16, pos: 'ST', group: 'FWD', sub: true },
];

const FIRST = [
  'Alex', 'Marco', 'Lucas', 'Noah', 'Omar', 'Yuki', 'Diego', 'Ivan', 'Kenji', 'Mateo',
  'Sven', 'Hassan', 'Pierre', 'Jan', 'Erik', 'Carlos', 'André', 'Luis', 'Tom', 'Sam',
];
const LAST = [
  'Silva', 'Kim', 'Novák', 'García', 'Müller', 'Okonkwo', 'Petrov', 'Ali', 'Tanaka', 'Rossi',
  'Johansson', 'Benali', 'Dupont', 'Horváth', 'Nielsen', 'Santos', 'Weber', 'Costa', 'Lee', 'Brown',
];

function hash(s) {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (Math.imul(31, h) + s.charCodeAt(i)) | 0;
  return Math.abs(h);
}

function parseTeamsFromMigration(sql) {
  const re = /\('(team-w26-[^']+)',\s*'([^']*)',\s*'([^']*)',\s*'([^']*)'/g;
  const teams = [];
  let m;
  while ((m = re.exec(sql)) !== null) {
    teams.push({ id: m[1], name: m[2], short: m[3], country: m[4] });
  }
  return teams;
}

const drawSql = readFileSync(
  path.join(__dirname, '../migrations/0020_fifa_official_draw_2026.sql'),
  'utf8',
);
const teams = parseTeamsFromMigration(drawSql);
if (teams.length !== 48) {
  console.warn(`Expected 48 teams, found ${teams.length}`);
}

const lines = [
  '-- WC 2026 squads: 16 players per nation (11 roster core + 4 bench)',
  '-- Generated by scripts/generate-wc2026-squad-migration.mjs',
  '',
];

for (const team of teams) {
  const squadId = `sq-${team.id.replace('team-', '')}`;
  lines.push(
    `INSERT OR IGNORE INTO squads (id, tournament_id, team_id, source_id, is_official, confidence, announced_at)`,
    `VALUES ('${squadId}', 't-2026', '${team.id}', 'src-mock', 1, 0.82, '2026-05-15T12:00:00Z');`,
    '',
  );

  for (const slot of ROSTER) {
    const pid = `p-${team.id.replace('team-', '')}-${String(slot.shirt).padStart(2, '0')}`;
    const h = hash(`${team.id}:${slot.shirt}`);
    const name = `${FIRST[h % FIRST.length]} ${LAST[(h + slot.shirt) % LAST.length]}`;
    const safeName = name.replace(/'/g, "''");
    lines.push(
      `INSERT OR IGNORE INTO players (id, name, nationality, primary_team_id, club, position, role_tags_json, age)`,
      `VALUES ('${pid}', '${safeName}', '${team.country}', '${team.id}', NULL, '${slot.group === 'GK' ? 'GK' : slot.group === 'DEF' ? 'DF' : slot.group === 'MID' ? 'MF' : 'FW'}', '[]', ${22 + (h % 12)});`,
    );
    lines.push(
      `INSERT OR IGNORE INTO squad_players (squad_id, player_id, shirt_number, listed_position, status)`,
      `VALUES ('${squadId}', '${pid}', ${slot.shirt}, '${slot.pos}', 'available');`,
    );
  }
  lines.push('');
}

const out = path.join(__dirname, '../migrations/0022_wc2026_team_squads.sql');
writeFileSync(out, lines.join('\n'));
console.log(`Wrote ${out} — ${teams.length} squads, ${teams.length * ROSTER.length} players`);
